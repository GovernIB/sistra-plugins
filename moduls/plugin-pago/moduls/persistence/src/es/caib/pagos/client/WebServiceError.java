package es.caib.pagos.client;

public class WebServiceError {
	
	private String error = null;

	
	
	public WebServiceError(String error) {
		super();
		this.error = error;
	}

	public String getError() {
		return error;
	}

	public void setError(String error) {
		this.error = error;
	}
	
	

}
